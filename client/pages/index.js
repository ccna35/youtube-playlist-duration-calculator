import Head from "next/head";
import Image from "next/image";
import Navbar from "../components/Navbar";
import Footer from "../components/Footer";
import styles from "../styles/Home.module.css";
import { useState } from "react";

export default function Home() {
  const [playlistID, setPlaylistID] = useState("");
  const [data, setData] = useState({});
  const [loading, setLoading] = useState(true);

  const getPlaylistDuration = async (e) => {
    e.preventDefault();
    setData({});
    const res = await fetch(`http://localhost:8080/${playlistID}`);
    const json = await res.json();
    setData(json);
    setPlaylistID("");
    setLoading(false);
  };

  return (
    <>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
        <title>YouTube Playlist Duration Calculator</title>
      </Head>
      <main className={styles.main}>
        <div className={styles.container}>
          <form
            className={styles.form}
            onSubmit={(e) => getPlaylistDuration(e)}
          >
            <input
              type="text"
              placeholder="youtube.com/playlist?list=ID"
              onChange={(e) => {
                setPlaylistID(e.target.value);
              }}
              value={playlistID}
            />
            <input type="submit" value="Get Full Duration" />
          </form>
          <div className={styles.playlistData}>
            {loading ? (
              "Loading data..."
            ) : (
              <div>
                {data.days !== 0 && <p>Days: {data.days}</p>}
                <p> Hours: {data.hours}</p>
                <p> Minutes: {data.minutes}</p>
                <p> Seconds: {data.seconds}</p>
              </div>
            )}
          </div>
          <div>
            Lorem ipsum dolor, sit amet consectetur adipisicing elit. Sapiente
            totam doloribus, ex adipisci nam animi debitis odio, pariatur
            necessitatibus eligendi doloremque commodi minima repudiandae
            expedita ab quas aperiam! Non, incidunt.
          </div>
        </div>
      </main>
    </>
  );
}
